package day09_stringequals_conditionals;

public class Languages {

	public static void main(String[] args) {

		int option = 2;
		
		if(option == 1) {
			System.out.println("Hello!");
		}
		
		if(option == 2) {
			System.out.println("Hola!");
		}
		
		if(option == 3) {
			System.out.println("Bonjour!");
		}
		
		if(option == 4) {
			System.out.println("Merhaba!");
		}
		
		
		
	}

}
