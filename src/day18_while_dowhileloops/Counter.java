package day18_while_dowhileloops;

public class Counter {

	public static void main(String[] args) {

		int num = 1;
		int n2 = 10;
		
		while(num < n2) {
			System.out.print(num + ", ");
			num++;
		}
		
		int num2 = 10;
		
		while(num2 > 0) {
			System.out.print(num2 + ", ");
			num2--;
		}
		
	}

}
