package day06_operators2;

public class IncrementDecrement {

	public static void main(String[] args) {
		
		int employees = 50;
		System.out.println("Employees: " + employees);
		
		employees++; // Increments the employees variable by 1
		System.out.println("Employees: " + employees);

		employees--; // Decrements the employees variable by 1
		System.out.println("Employees: " + employees);
		
		// We use auto increment/decrement when we want to increase/decrease a variable by 1
		
	}

}
